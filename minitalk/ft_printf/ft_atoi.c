#include "ft_printf.h"

int	ft_atoi(const char *s)
{
	int	sign;
	int	all;
	int	i;

	i = 0;
	all = 0;
	sign = 1;
	while (s[i] == ' ' || s[i] == '\n' || s[i] == '\v'
		|| s[i] == '\t' || s[i] == '\r' || s[i] == '\f')
		i++;
	if (s[i] == '+' || s[i] == '-')
	{
		if (s[i] == '-')
			sign *= -1;
		i++;
	}
	while ((s[i] >= '0') && (s[i] <= '9'))
	{
		all = all * 10 + s[i] - '0';
		i++;
	}
	return (all * sign);
}

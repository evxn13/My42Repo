#include "push_swap.h"

int	parsing(t_variable *var)
{
	int	i;

	i = 0;
	while (i != var->len_a -1)
	{
		if (var->a[i] > var->a[i + 1] && var->a[i + 1])
			return (1);
		i++;
	}
	return (0);
}
